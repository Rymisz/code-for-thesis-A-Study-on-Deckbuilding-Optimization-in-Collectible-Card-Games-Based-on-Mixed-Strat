import numpy as np

def eliminate_dominated_strategies(A, names):
    n = A.shape[0]
    active = list(range(n))
    changed = True
    
    # 迭代剔除直到没有劣势策略为止
    while changed:
        changed = False
        to_remove = set()
        
        for i in active:
            for k in active:
                if i != k and i not in to_remove and k not in to_remove:
                    # 判断策略 i 是否被策略 k 全面压制 (弱劣势)
                    if np.all(A[i, active] <= A[k, active]) and \
                       np.any(A[i, active] < A[k, active]):
                        to_remove.add(i)
                        changed = True
                        break
        
        # 更新存活的策略索引
        active = [x for x in active if x not in to_remove]
        
    # 提取降维后的核心矩阵与卡组名称
    A_reduced = A[np.ix_(active, active)]
    names_reduced = [names[i] for i in active]
    return A_reduced, names_reduced

